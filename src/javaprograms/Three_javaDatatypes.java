
// There are 8 types of primitive data types:
//
// boolean data type
// byte data type
// char data type
// short data type
// int data type
// long data type
// float data type
// double data type


package javaprograms;

public class Three_javaDatatypes {
	
	
	boolean bool_var  = true;     // 1 bit
	byte byte_var 	  = 10;		  // 1 byte
	char char_var     = 'a';      // 2 byte
	short short_var   = 1000;     // 2 byte
	int int_var       = 2; 		  // 4 byte
	long long_var     = 1000L;    // 8 byte
	float float_var   = 70.6f;   // 4 byte
	double double_var = 12.3d;    // 8 byte
	
			
}
